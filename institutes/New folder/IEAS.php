        <div class="line"></div>
        <div class="single-service">
          <span></span>
        <h3 style="font-family: Oswald; letter-spacing:1px;">PROGRAM DESCRIPTION</h3>
        <p><p>The Bachelor of Special Needs Education (BSNEd) is an undergraduate degree program which specializes in special needs education.</p>
        <p><p>The program goal is to prepare teachers who will instruct and manage students with additional needs in inclusive and segregated educational settings. Its graduates will be equipped to teach the basic education curriculum and alternate curricula depending on the needs of the students. Those who will teach in the elementary level will be trained across different areas. Those who will teach in the secondary level will provide educational support to students with additional needs in inclusive classrooms or provide educational services to students who may be enrolled in special education centers.</p>
        <p><p>A graduate of this degree program should be able to teach in regular and special education schools as a teacher provided that he/she passes the Licensure Examination for Teachers in his/her subject area of specialization.</p></p>
