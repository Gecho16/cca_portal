        <h2>Bachelor of Arts in English Language Studies</h2>
        <div class="line"></div>
        <div class="single-service">
          <span></span>
        <h3 style="font-family: Oswald; letter-spacing:1px;">PROGRAM DESCRIPTION</h3>
        <p><p>The BAELS is a four-year degree program designed to integrate theory and practice to prepare students the students for effective communication in English in diverse contexts and situations.</p>
        <p><p>Graduates possess the following: broad knowledge of language and literature for learning; use English as a global language in a multilingual context as it applies to the teaching of language and literature; acquire extensive reading background in language and literature; demonstrate proficiency in oral and written communication; show competence in employing innovative language and literature approaches, methodologies, and strategies; use technology in facilitating language learning and teaching; and display skills and abilities to be a reflective and research-oriented language and literature teacher.</p>
        <p><p>It aims: to provide a comprehensive knowledge of the English language – its origin, growth and development, structures, and use; to enhance the students’ competencies in the use of the English language in the real-world contexts; and to present appropriate strategies of language use through a heightened awareness of how English works in different situations in the Philippines and in Asia and the rest of the world.</p></p>
