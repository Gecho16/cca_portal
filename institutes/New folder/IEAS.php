        <h5 style="color:rgb(61, 44, 12); font-family: Oswald;">Graduates of this program may opt for the following careers:</h5><p><ul>
<li>Academic and scholarly work, such as teaching, instructional materials development, and research</li>
<li>Business and industry jobs, such as public relations, advertising, and documentation</li>
<li>Media and publishing occupations, such as hosting, writing, proofreading, translating, and editing</li>
<li>Government and diplomatic service, such as law, foreign service, and tourism</li></ul></p>

