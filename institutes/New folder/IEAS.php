<li>Teachers in the field of Special Needs Education</li>
<li>May be employed in special education centers/clinics as special education teachers, therapists, or clinicians</li>
<li>Learning support staff clinical, counseling, industrial/organization, among others and in other professions such as law, medicine, and business management</li></ul></p>

