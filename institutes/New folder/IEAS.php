          <h3 style="font-family: Oswald; letter-spacing:1px;">PROGRAM DESCRIPTION</h3>
          <p><p>The Bachelor of Physical Education or BPEd is a (4) four-year program aimed at equipping graduates with the competencies to meet the psychomotor, cognitive and affective needs of learners.</p></p>
          <h3 style="font-family: Oswald; letter-spacing:.5px;">The program aims to prepare its graduates</h3><p><ul>
	<li>strong and substantial foundation of the subject matter (disciplinary knowledge) that informs their curricular choices when planning, designing, implementing and assessing learning activities (curriculum and program planning, implementation, monitoring, and evaluation).</li>
  <li>an understanding of the scope and sequence of various movement forms; as well as elements, strategies and tactics of these various movements in a multitude of settings (movement competency and proficiency) that enables them to meet the needs of learners to know how, be able to do and how to learn.</li>
	<li>expert knowledge of pedagogy for maximizing student engagement, monitoring students and modeling respect for differences in gender, ability and culture.</li>
	<li>reflective practice that (a) propels them to set high standards for and hold themselves accountable to the professional standards (professional accountability and responsibility); (b) enables them to identify gaps in their current competencies and pursue professional development opportunities; and (c) assists them in studying the impact of their teaching on student learning.</li>
  <li>building and cultivating relationships with colleagues, stakeholders, other professionals and learning communities; advocating for PE, being role models of integrity and professional excellence, as well as leaders in the service of education.</li></ul></p>                    
